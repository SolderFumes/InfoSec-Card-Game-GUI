Map::Map() {
	dirt = TextureManager::LoadTexture("dirt.png");
	grass = TextureManager::LoadTexture("grass.png");
	water = TextureManager::LoadTexture("water.png");

	srcRect.x = srcRect.y = 0;
	srcRect.h = destRect.h = 32;
	srcRect.w = destRect.w = 32;

	destRect.x = destRect.y = 0;

	loadMap(lvl1);

}

Map::~Map() {

}

void Map::loadMap(int arr[20][25]) {
	for (int r = 0; r < 20; r++) {
		for (int c = 0; c < 25; c++) {
			map[r][c] = arr[r][c];
		}
	}
}

void Map::drawMap() {
	int type = 0;
	for (int r = 0; r < 20; r++) {
		for (int c = 0; c < 25; c++) {
			type = map[r][c];

			//draw in 32 pixel chunks with each element in Map being a 32x32 pixel chunk
			destRect.x = c * 32;
			destRect.y = r * 32;

			switch (type) {
			case 0:
				TextureManager::Draw(water, srcRect, destRect);
				break;
			case 1:
				TextureManager::Draw(grass, srcRect, destRect);
				break;
			case 2:
				TextureManager::Draw(dirt, srcRect, destRect);
				break;
			default:
				break;
			}

		}
	}
}